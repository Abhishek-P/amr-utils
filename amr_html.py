
from amr import AMR
import re, sys

class AMR_HTML:

    @staticmethod
    def html(text):
        amr = AMR(text)
        elems = [e for e in amr.elements()]
        nodes = [id for id in amr.node_ids()]
        edges = [id for id in amr.edge_ids()]
        node_indices = [j for j in amr.node_indices()]
        edge_indices = [j for j in amr.edge_indices()]
        for i,e in enumerate(elems):
            if i in node_indices:
                id = nodes.pop(0)
                elems[i] = f'<span class="amr-node" tok-id="{id}">{e}</span>'
            elif i in edge_indices:
                id = edges.pop(0)
                elems[i] = f'<span class="amr-edge" tok-id="{id}">{e}</span>'
        text = ''.join(elems)
        return '\n<div class="amr-container">\n<pre>\n'+text+'\n</pre>\n</div>\n'

    @staticmethod
    def html_with_links(text):
        amr = AMR(text)
        elems = [e for e in amr.elements()]
        nodes = [id for id in amr.node_ids()]
        edges = [id for id in amr.edge_ids()]
        node_indices = [j for j in amr.node_indices()]
        edge_indices = [j for j in amr.edge_indices()]
        for i, e in enumerate(elems):
            if i in node_indices:
                id = nodes.pop(0)
                link = AMR_HTML.propbank_link(e)
                if link:
                    elems[i] = f'<a href="{link}" class="amr-node" tok-id="{id}">{e}</a>'
                else:
                    elems[i] = f'<span class="amr-node" tok-id="{id}">{e}</span>'
            elif i in edge_indices:
                id = edges.pop(0)
                link= AMR_HTML.amr_rel_link(e)
                if link:
                    elems[i] = f'<a  href="{link}" class="amr-edge" tok-id="{id}">{e}</a>'
                else:
                    elems[i] = f'<span class="amr-edge" tok-id="{id}">{e}</span>'
        text = ''.join(elems)
        return '\n<div class="amr-container">\n<pre>\n'+text+'\n</pre>\n</div>\n'

    @staticmethod
    def propbank_link(frame):
        if not '/' in frame:
            return ''
        frame = frame.split('/')[1]
        x = re.match('(?P<f>.*)-[0-9]+',frame)
        if not x:
            return ''
        frame = x.group('f')
        if frame in AMR_HTML.prop_frames_dictionary:
            return AMR_HTML.prop_frames_url+frame+'.html'
        return ''

    @staticmethod
    def amr_rel_link(rel):
        if '#'+rel in AMR_HTML.rel_dictionary:
            return AMR_HTML.amr_dictionary_url + '#'+rel
        return ''


